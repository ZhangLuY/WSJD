package com.zrhis.wsjd.exception;

public class WSJDDaoException extends Exception{
	
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 1901344100777586483L;

	public WSJDDaoException() {
		super();
	}

	public WSJDDaoException(String message, Throwable cause) {
		super(message, cause);
	}

	public WSJDDaoException(String message) {
		super(message);
	}

	public WSJDDaoException(Throwable cause) {
		super(cause);
	}
}
