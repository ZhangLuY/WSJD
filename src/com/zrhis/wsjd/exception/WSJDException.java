package com.zrhis.wsjd.exception;

public class WSJDException extends Exception{
	
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 1901344100777586483L;

	public WSJDException() {
		super();
	}

	public WSJDException(String message, Throwable cause) {
		super(message, cause);
	}

	public WSJDException(String message) {
		super(message);
	}

	public WSJDException(Throwable cause) {
		super(cause);
	}
}
